L10n l10nOf(AppLocale locale) {
  switch (locale) {
    case AppLocale.tr:
      return _tr;
    case AppLocale.en:
      return _en;
    case AppLocale.es:
      return _es;
    case AppLocale.ru:
      return _ru;
  }
}

