-- (b) reviews → locations.rating_avg / rating_count / review_count
-- (yalnız status='approved' AND deleted_at IS NULL olan yorumlar sayılır)
CREATE OR REPLACE FUNCTION refresh_location_review_stats(p_location_id UUID) RETURNS void
LANGUAGE plpgsql AS $fn$
BEGIN
  UPDATE locations SET
    rating_avg = stats.avg_rating,
    rating_count = stats.cnt,
    review_count = stats.cnt
  FROM (
    SELECT AVG(overall_rating)::numeric(3, 2) AS avg_rating, COUNT(*)::int AS cnt
    FROM reviews
    WHERE location_id = p_location_id AND status = 'approved' AND deleted_at IS NULL
  ) AS stats
  WHERE locations.id = p_location_id;
END;
$fn$;

CREATE OR REPLACE FUNCTION trg_reviews_refresh_location_stats() RETURNS trigger
LANGUAGE plpgsql AS $fn$
BEGIN
  IF TG_OP = 'DELETE' THEN
    PERFORM refresh_location_review_stats(OLD.location_id);
  ELSIF TG_OP = 'UPDATE' THEN
    PERFORM refresh_location_review_stats(NEW.location_id);
    IF NEW.location_id IS DISTINCT FROM OLD.location_id THEN
      PERFORM refresh_location_review_stats(OLD.location_id);
    END IF;
  ELSE
    PERFORM refresh_location_review_stats(NEW.location_id);
  END IF;
  RETURN COALESCE(NEW, OLD);
END;
$fn$;

CREATE TRIGGER trg_reviews_stats
  AFTER INSERT OR UPDATE OR DELETE ON reviews
  FOR EACH ROW EXECUTE FUNCTION trg_reviews_refresh_location_stats();

-- (c) location_media değişince locations.photo_count güncelle (yalnız approved medya)
CREATE OR REPLACE FUNCTION trg_location_media_refresh_photo_count() RETURNS trigger
LANGUAGE plpgsql AS $fn$
DECLARE
  v_location_id UUID;
BEGIN
  v_location_id := COALESCE(NEW.location_id, OLD.location_id);
  UPDATE locations SET photo_count = (
    SELECT COUNT(*) FROM location_media lm
    JOIN media m ON m.id = lm.media_id
    WHERE lm.location_id = v_location_id AND m.moderation_status = 'approved' AND m.deleted_at IS NULL
  ) WHERE id = v_location_id;
  RETURN COALESCE(NEW, OLD);
END;
$fn$;

CREATE TRIGGER trg_location_media_photo_count
  AFTER INSERT OR UPDATE OR DELETE ON location_media
  FOR EACH ROW EXECUTE FUNCTION trg_location_media_refresh_photo_count();

-- (d) review_reactions → reviews.helpful_count
CREATE OR REPLACE FUNCTION trg_review_reactions_refresh_helpful_count() RETURNS trigger
LANGUAGE plpgsql AS $fn$
DECLARE
  v_review_id UUID;
BEGIN
  v_review_id := COALESCE(NEW.review_id, OLD.review_id);
  UPDATE reviews SET helpful_count = (
    SELECT COUNT(*) FROM review_reactions WHERE review_id = v_review_id AND reaction = 'helpful'
  ) WHERE id = v_review_id;
  RETURN COALESCE(NEW, OLD);
END;
$fn$;

CREATE TRIGGER trg_review_reactions_helpful_count
  AFTER INSERT OR UPDATE OR DELETE ON review_reactions
  FOR EACH ROW EXECUTE FUNCTION trg_review_reactions_refresh_helpful_count();

