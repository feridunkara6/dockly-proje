-- check_in_on >= CURRENT_DATE yalnızca INSERT anında geçerlidir (§5.9);
-- normal CHECK constraint olsaydı tarih geçtikçe eski satırların
-- UPDATE'i (ör. status değişimi) haksız yere reddedilirdi.
CREATE OR REPLACE FUNCTION trg_check_reservation_request_checkin() RETURNS trigger
LANGUAGE plpgsql AS $fn$
BEGIN
  IF NEW.check_in_on < CURRENT_DATE THEN
    RAISE EXCEPTION 'check_in_on geçmiş bir tarih olamaz (check_in_on=%)', NEW.check_in_on;
  END IF;
  RETURN NEW;
END;
$fn$;

CREATE TRIGGER trg_reservation_requests_checkin_insert
  BEFORE INSERT ON reservation_requests
  FOR EACH ROW EXECUTE FUNCTION trg_check_reservation_request_checkin();

