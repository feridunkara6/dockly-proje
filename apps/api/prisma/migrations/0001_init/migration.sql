-- district'in parent'ı province olmalı: bu, parent satırının level'ına
-- bakmayı gerektirir (satır-içi CHECK ile ifade edilemez) → trigger.
CREATE OR REPLACE FUNCTION trg_check_admin_area_hierarchy() RETURNS trigger
LANGUAGE plpgsql AS $fn$
DECLARE
  v_parent_level admin_area_level;
BEGIN
  IF NEW.level = 'district' THEN
    IF NEW.parent_id IS NULL THEN
      RAISE EXCEPTION 'district seviyesindeki admin_area için parent_id (province) zorunludur';
    END IF;
    SELECT level INTO v_parent_level FROM admin_areas WHERE id = NEW.parent_id;
    IF v_parent_level IS DISTINCT FROM 'province' THEN
      RAISE EXCEPTION 'district admin_area''in parent''ı province olmalıdır (bulunan seviye: %)', v_parent_level;
    END IF;
  END IF;
  RETURN NEW;
END;
$fn$;

CREATE TRIGGER trg_admin_areas_hierarchy
  BEFORE INSERT OR UPDATE ON admin_areas
  FOR EACH ROW EXECUTE FUNCTION trg_check_admin_area_hierarchy();

