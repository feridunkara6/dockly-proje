  SELECT lt.code INTO v_code FROM locations l JOIN location_types lt ON lt.id = l.location_type_id WHERE l.id = NEW.location_id;
  IF v_code IS NULL OR v_code NOT IN ('restaurant_pier') THEN
    RAISE EXCEPTION 'restaurant_dock_details yalnızca restaurant_pier tipli location için eklenebilir (location_id=%, type=%)', NEW.location_id, v_code;
  END IF;
  RETURN NEW;
END;
