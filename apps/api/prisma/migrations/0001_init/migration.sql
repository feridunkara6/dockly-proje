-- CHECK tutarlılığı: detay satırı yalnız uygun location_type için eklenebilir (§5.6.1 sonu).
CREATE OR REPLACE FUNCTION trg_check_marina_details_type() RETURNS trigger
LANGUAGE plpgsql AS $fn$
DECLARE v_code TEXT;
BEGIN
  SELECT lt.code INTO v_code FROM locations l JOIN location_types lt ON lt.id = l.location_type_id WHERE l.id = NEW.location_id;
  IF v_code IS NULL OR v_code NOT IN ('private_marina', 'municipal_marina', 'boat_club') THEN
    RAISE EXCEPTION 'marina_details yalnızca private_marina/municipal_marina/boat_club tipli location için eklenebilir (location_id=%, type=%)', NEW.location_id, v_code;
  END IF;
  RETURN NEW;
END;
$fn$;

CREATE TRIGGER trg_marina_details_check_type
  BEFORE INSERT OR UPDATE ON marina_details
  FOR EACH ROW EXECUTE FUNCTION trg_check_marina_details_type();

