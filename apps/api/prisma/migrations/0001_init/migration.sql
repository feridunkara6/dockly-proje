-- to_tsvector(regconfig, text) STABLE'dır (regconfig ALTER edilebilir
-- olduğu için); GENERATED ALWAYS ... STORED yalnızca IMMUTABLE ifadeler
-- kabul eder. 'simple' konfigürasyonu pratikte hiç değişmediğinden bu
-- ince sarmalayıcı güvenle IMMUTABLE işaretlenir (locations.search_text
-- generated kolonunun tek çalışır yolu budur).
CREATE OR REPLACE FUNCTION immutable_simple_tsvector(p_text text) RETURNS tsvector
LANGUAGE sql IMMUTABLE PARALLEL SAFE AS $fn$
  SELECT to_tsvector('simple', p_text);
$fn$;

